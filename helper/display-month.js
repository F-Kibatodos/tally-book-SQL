module.exports = displayMonth => {
  if (displayMonth === '-01-') return (displayMonth = '一月')
  else if (displayMonth === '-02-') return (displayMonth = '二月')
  else if (displayMonth === '-03-') return (displayMonth = '三月')
  else if (displayMonth === '-04-') return (displayMonth = '四月')
  else if (displayMonth === '-05-') return (displayMonth = '五月')
  else if (displayMonth === '-06-') return (displayMonth = '六月')
  else if (displayMonth === '-07-') return (displayMonth = '七月')
  else if (displayMonth === '-08-') return (displayMonth = '八月')
  else if (displayMonth === '-09-') return (displayMonth = '九月')
  else if (displayMonth === '-10-') return (displayMonth = '十月')
  else if (displayMonth === '-11-') return (displayMonth = '十一月')
  else if (displayMonth === '-12-') return (displayMonth = '十二月')
}
